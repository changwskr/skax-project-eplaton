package com.kbstar.ksa.das;

/**
 * New PersistenceException class
 * 
 * This is a stub implementation to replace the missing KSA framework
 * PersistenceException
 */
public class NewPersistenceException extends Exception {

    public NewPersistenceException() {
        super();
    }

    public NewPersistenceException(String message) {
        super(message);
    }

    public NewPersistenceException(Throwable cause) {
        super(cause);
    }

    public NewPersistenceException(String message, Throwable cause) {
        super(message, cause);
    }
}