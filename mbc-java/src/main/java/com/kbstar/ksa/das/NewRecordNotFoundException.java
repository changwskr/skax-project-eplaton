package com.kbstar.ksa.das;

/**
 * New RecordNotFoundException class
 * 
 * This is a stub implementation to replace the missing KSA framework
 * RecordNotFoundException
 */
public class NewRecordNotFoundException extends Exception {

    public NewRecordNotFoundException() {
        super();
    }

    public NewRecordNotFoundException(String message) {
        super(message);
    }

    public NewRecordNotFoundException(Throwable cause) {
        super(cause);
    }

    public NewRecordNotFoundException(String message, Throwable cause) {
        super(message, cause);
    }
}